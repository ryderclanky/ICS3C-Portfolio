def sub(num1, num2):
    """
    -------------------------------------------------------
    Takes two numbers and subtracts them
    -------------------------------------------------------
    Parameters:
        num1 - first number from user  (int)
        num2 - second number from user (int)
    Returns:
        result - value of the result of num1 and num2 being subtracted (int)
    -------------------------------------------------------
    """
    result = num1 - num2
    return result


answer = sub(10, 4)
print(answer)
